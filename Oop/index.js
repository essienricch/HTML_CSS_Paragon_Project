class Account{

    constructor(firstName, lastName, age, gender){
        this.firstName = firstName
        this.lastName = lastName
        this.age = age
        this.gender = gender
    }

    setFirstName(firstName){
        this.firstName = firstName
    }
    getFirstName(){
        return this.firstName
    }

    setLastName(lastName){
        this.lastName = lastName
    }
    getLastName(){
        return this.lastName
    }

    setAge(age){
        this.age = age
    }
    getAge(){
        return this.age
    }

    setGender(gender){
        this.gender = gender
    }
    getGender(){
        return this.gender
    }




}